function getLocalized(language, key, ...args) {
  const value = messages[language]?.[key] ?? messages.en[key] ?? key;
  return typeof value === "function" ? value(...args) : value;
}

function t(key, ...args) {
  const language = messages[config.language] ? config.language : "en";
  return getLocalized(language, key, ...args);
}

function bilingual(key, ...args) {
  return {
    en: getLocalized("en", key, ...args),
    ar: getLocalized("ar", key, ...args)
  };
}

